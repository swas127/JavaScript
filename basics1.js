// Modulus Function
function modulusExample(a, b) {
    console.log(`${a} % ${b} =`, a % b); // Returns the remainder of a divided by b
}

// Exponentiation Function
function exponentiationExample(base, exponent) {
    console.log(`${base} ** ${exponent} =`, base ** exponent); // Returns base raised to the power of exponent
}

// Example Usage
modulusExample(10, 3); // Output: 10 % 3 = 1
modulusExample(15, 4); // Output: 15 % 4 = 3

exponentiationExample(2, 3); // Output: 2 ** 3 = 8
exponentiationExample(5, 2); // Output: 5 ** 2 = 25